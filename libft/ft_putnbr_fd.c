#include "./include/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	m;

	m = (unsigned int)n;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		m *= -1;
	}
	if (m >= 10)
	{
		ft_putnbr_fd(m / 10, fd);
		ft_putnbr_fd(m % 10, fd);
	}
	else
	{
		ft_putchar_fd('0' + m, fd);
	}
}
