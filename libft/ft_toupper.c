#include "./include/libft.h"

int	ft_toupper(int c)
{
	if (c >= 'A' && c <= 'Z')
	{
		return (c);
	}
	else if (c >= 'a' && c <= 'z')
	{
		return (c - ('a' - 'A'));
	}
	else
	{
		return (c);
	}
}
